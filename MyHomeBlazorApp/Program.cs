using Microsoft.AspNetCore.Components;
using Microsoft.AspNetCore.Components.Web;
using Microsoft.Extensions.FileProviders;
using MyHomeBlazorApp.BlazorData;
using System.Security.Cryptography.X509Certificates;
using Microsoft.AspNetCore.Identity;
using Microsoft.EntityFrameworkCore;
using Microsoft.AspNetCore.Components.Authorization;
using MyHomeBlazorApp.Components.Account;
using Microsoft.AspNetCore.Builder;
using Microsoft.Data.Sqlite;
using Microsoft.AspNetCore.Identity.UI.Services;
using WebPWrecover.Services;

//using MyHomeBlazorApp.Components.Account;
namespace MyHomeBlazorApp
{
    public class Program
    {
        //TODO: create autogenerated path
        public class Constants
        {
            public static readonly string RootPath = AppContext.BaseDirectory;
            public const string XML_DATA_PATH = @"C:\Temp\usersListTestData111.xml";
            public static readonly string SAVE_QR_CODE_PATH = Path.Combine(RootPath, "Files", "qrCode.png");
            public static readonly string SAVE_UPLOADED_FILES = Path.Combine(RootPath, "Files", "UploadedFiles") + Path.DirectorySeparatorChar;
            public const string BASE_API_URL = "https://www.google.se/search?q=";
        }
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);
            var connectionString = builder.Configuration.GetConnectionString("MyHomeBlazorAppContextConnection") ?? throw new InvalidOperationException("Connection string 'MyHomeBlazorAppContextConnection' not found.");

            builder.Services.AddDbContext<MyHomeBlazorAppContext>(options => options.UseSqlite(connectionString));


            builder.Services.AddRazorComponents()
        .AddInteractiveServerComponents();

            builder.Services.AddCascadingAuthenticationState();

            builder.Services.AddScoped<IdentityUserAccessor>();
            builder.Services.AddScoped<IdentityRedirectManager>();
            builder.Services.AddScoped<AuthenticationStateProvider, IdentityRevalidatingAuthenticationStateProvider>();

            builder.Services.AddAuthentication(options =>
            {
                options.DefaultScheme = IdentityConstants.ApplicationScheme;
                options.DefaultSignInScheme = IdentityConstants.ExternalScheme;
            })
    .AddIdentityCookies();


            builder.Services.AddIdentityCore<MyHomeBlazorAppUser>(options =>
            {
                options.SignIn.RequireConfirmedAccount = false; 
                options.SignIn.RequireConfirmedEmail = false;
            })
                .AddRoles<IdentityRole>()
                .AddEntityFrameworkStores<MyHomeBlazorAppContext>()
                .AddSignInManager()
                .AddDefaultTokenProviders()
                .AddApiEndpoints();

            builder.Services.ConfigureApplicationCookie(options =>
            {
                options.Cookie.HttpOnly = true;
                options.Cookie.SecurePolicy = CookieSecurePolicy.SameAsRequest;
                options.Cookie.SameSite = SameSiteMode.Lax;
            });

            // Add services to the container.
            builder.Services.AddRazorPages();
            builder.Services.AddServerSideBlazor();
            builder.Services.AddScoped<DataService>();
            builder.Services.AddAuthorizationCore();
            builder.Services.AddBlazorBootstrap();
            builder.Services.Configure<AuthMessageSenderOptions>(builder.Configuration.GetSection("AuthMessageSenderOptions"));
            builder.Services.AddTransient<IEmailSender<MyHomeBlazorAppUser>, EmailSender>();
            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Error");
                // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
                app.UseHsts();
            }


            //app.UseHttpsRedirection(); 
            app.UseStaticFiles();
            // routing url to physical path "Files"
            var filesPath = Path.Combine(builder.Environment.ContentRootPath, "Files");

            // Critical: Create the directory if it's missing to prevent the crash
            if (!Directory.Exists(filesPath))
            {
                Directory.CreateDirectory(filesPath);
            }

            app.UseStaticFiles(new StaticFileOptions
            {
                FileProvider = new PhysicalFileProvider(filesPath),
                RequestPath = "/Files"
            });

            //      app.UseRouting();
            app.UseAuthentication();
            app.UseAuthorization();
            app.UseAntiforgery();
            //      app.MapControllers();
            //      app.MapBlazorHub();
            app.MapIdentityApi<MyHomeBlazorAppUser>();
            app.MapRazorComponents<App>()
               .AddInteractiveServerRenderMode();
            app.MapAdditionalIdentityEndpoints();

            using (var scope = app.Services.CreateScope())
            {
                var services = scope.ServiceProvider;
                try
                {
                    var context = services.GetRequiredService<MyHomeBlazorAppContext>();
                    // This command creates the database file and all tables (AspNetUsers, etc.)
                    context.Database.Migrate();
                    Console.WriteLine("Database migration applied successfully.");
                }
                catch (Exception ex)
                {
                    Console.WriteLine($"An error occurred during migration: {ex.Message}");
                }
            }

            app.Run();
        }
    }
}